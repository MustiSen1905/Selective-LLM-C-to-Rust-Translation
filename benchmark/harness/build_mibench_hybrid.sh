#!/usr/bin/env bash
# Build V-c2rust and V-hybrid variants for MiBench projects.
#
# Reads Rust output from:
#   Rust-Outcome/mibench_<NAME>/rust_out_c2rust_checkpoint/  → V-c2rust
#   Rust-Outcome/mibench_<NAME>/rust_out/                    → V-hybrid
#
# Writes binaries to C-projects/mibench-master/bin/ as:
#   <NAME>_c2rust   <NAME>_hybrid
#
# Run AFTER:
#   1. python3 translation/mibench_pipeline.py --all  [--stop-after-c2rust]
#   2. (optional) LLM stage has finished for V-hybrid
#
# Usage (from project root or WSL):
#   bash benchmark/harness/build_mibench_hybrid.sh            # all 11 projects
#   bash benchmark/harness/build_mibench_hybrid.sh basicmath  # one project
#
# If you get "pipefail: invalid option":
#   sed -i 's/\r//' benchmark/harness/build_mibench_hybrid.sh
set -uo pipefail

WT=$(cd "$(dirname "$0")/../.." && pwd)
MIBENCH="$WT/C-projects/mibench-master"
BIN="$MIBENCH/bin"
mkdir -p "$BIN"

# Source cargo env (WSL / Linux without shell profile)
source ~/.cargo/env 2>/dev/null || true

ALL_PROJECTS=(basicmath bitcount qsort_small susan dijkstra patricia stringsearch blowfish sha crc fft)

if [[ $# -ge 1 ]]; then
  PROJECTS=("$1")
else
  PROJECTS=("${ALL_PROJECTS[@]}")
fi

ok=0; fail=0; skipped=0

# ---------------------------------------------------------------------------
# build_staticlib_variant NAME LABEL SRC_DIR OUT_NAME
#   cargo build --lib  →  link safe_*.c + libhybrid_project.a  →  BIN/OUT_NAME
# ---------------------------------------------------------------------------
build_staticlib_variant() {
  local name="$1" label="$2" src_dir="$3" out_name="$4"

  printf "\n--- %-18s [V-%s] ---\n" "$name" "$label"

  if [[ ! -d "$src_dir" ]]; then
    echo "  SKIP: $src_dir not found (run mibench_pipeline.py first)."
    skipped=$((skipped + 1))
    return 0
  fi

  # Require lib.rs — if absent, c2rust produced no Rust output (empty project)
  if [[ ! -f "$src_dir/lib.rs" ]]; then
    echo "  SKIP: no lib.rs in $src_dir (c2rust produced no Rust output)."
    skipped=$((skipped + 1))
    return 0
  fi

  # Force a fresh Linux build: remove any stale lib.a that may have been
  # compiled by the Windows MSVC toolchain (incompatible with WSL gcc).
  rm -f "$src_dir/target/release/libhybrid_project.a"

  # Build Rust staticlib
  echo "  cargo build --release --lib  in $src_dir"
  if ! (cd "$src_dir" && cargo build --release --lib 2>&1 | tail -5); then
    echo "  FAILED: cargo build exited non-zero"
    fail=$((fail + 1))
    return 0
  fi

  local lib="$src_dir/target/release/libhybrid_project.a"
  if [[ ! -f "$lib" ]]; then
    echo "  FAILED: $lib not found after cargo build"
    fail=$((fail + 1))
    return 0
  fi

  # Collect safe_*.c shims (generated by c2rust.py alongside Cargo.toml)
  local safe_c_files=()
  while IFS= read -r f; do
    safe_c_files+=("$f")
  done < <(find "$src_dir" -maxdepth 1 -name 'safe_*.c' | sort)

  if [[ ${#safe_c_files[@]} -eq 0 ]]; then
    # All functions translated to Rust — lib.rs exports #[no_mangle] main.
    # Link with --whole-archive so the Rust main symbol is found by the linker.
    echo "  No safe_*.c shims (fully-Rust project) — linking with --whole-archive"
    if gcc -O2 -DNDEBUG \
          -o "$BIN/$out_name" \
          -Wl,--whole-archive "$lib" -Wl,--no-whole-archive \
          -lpthread -ldl -lm 2>&1; then
      echo "  Built: $BIN/$out_name"
      ok=$((ok + 1))
    else
      echo "  FAILED: gcc linking (--whole-archive)"
      fail=$((fail + 1))
    fi
    return 0
  fi

  echo "  safe_*.c: ${safe_c_files[*]##*/}"

  if gcc -O2 -DNDEBUG \
        -o "$BIN/$out_name" \
        "${safe_c_files[@]}" "$lib" \
        -lpthread -ldl -lm 2>&1; then
    echo "  Built: $BIN/$out_name"
    ok=$((ok + 1))
  else
    echo "  FAILED: gcc linking"
    fail=$((fail + 1))
  fi
}

# ---------------------------------------------------------------------------
echo ""
echo "================================="
echo " MiBench Hybrid Build"
echo "================================="
printf "  Projects : %s\n" "${PROJECTS[*]}"
printf "  Bin dir  : %s\n" "$BIN"
echo ""

for NAME in "${PROJECTS[@]}"; do
  CHECKPOINT="$WT/Rust-Outcome/mibench_${NAME}/rust_out_c2rust_checkpoint"
  RUST_OUT="$WT/Rust-Outcome/mibench_${NAME}/rust_out"

  build_staticlib_variant "$NAME" "c2rust" "$CHECKPOINT" "${NAME}_c2rust"
  build_staticlib_variant "$NAME" "hybrid" "$RUST_OUT"   "${NAME}_hybrid"
done

echo ""
echo "================================="
printf "  Built: %d   Failed: %d   Skipped: %d\n" "$ok" "$fail" "$skipped"
echo "================================="
echo ""
echo "Binaries in $BIN:"
ls -lh "$BIN"/*_c2rust "$BIN"/*_hybrid 2>/dev/null \
  | grep -v '^total' || echo "  (none built yet)"
